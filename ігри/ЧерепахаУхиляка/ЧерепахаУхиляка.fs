     хрест1 <- Фігури.ДодатиЛінію(0, -8, 0, 8)
     хрест2 <- Фігури.ДодатиЛінію(-8, 0, 8, 0)
     Фігури.Перемістити(хрест1, x, y)
     Фігури.Перемістити(хрест2, x, y)
     Фігури.Перемістити(поз, гш - 100, 20)   
   Черепаха.ПереміститиВ(x, y)
