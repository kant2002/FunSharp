   рухається <- ложь
   крутиться <- ложь
   Готово()
   ГрафичнеВікно.КлавішаНатиснута <- Callback(НаКнопкаНатиснута)
   нехай тик = ложь
   Таймер.Интервал <- 1000 / 24
