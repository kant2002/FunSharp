   останнійпмс <- Годинник.МинуліМілісекунди
   iMin <- 0
   доки не виявленоЗіткнення зробити
     якщо рухається тоді
       якщо останняКнопка = "Left" тоді
         Черепаха.ПовернутиНалево()
