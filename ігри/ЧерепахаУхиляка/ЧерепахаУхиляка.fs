     інакше
       Програма.Затримка(100)      
та Ініціалізація () =
   ГрафичнеВікно.КолірФона <- Кольори.DodgerBlue
   ГрафичнеВікно.Ширина <- гш
   ГрафичнеВікно.Висота <- гв   
   минуло <- 0
   виявленоЗіткнення <- ложь
та НаТик () =
   якщо не крутиться тоді
     крутиться <- істина
     нехай пмс = Годинник.МинуліМілісекунди
     якщо пмс - останнійпмс > 500 тоді
       ДодатиОб'єкт()
       останнійпмс <- пмс    
     КрутитиОб'єкт()
     крутиться <- ложь
   якщо відлагоджування тоді
     нехай x = Математика.Floor(Черепаха.X)
     нехай y = Математика.Floor(Черепаха.Y)
     Фігури.УстановитьТекст(поз, "(" + x.ToString() + "," + y.ToString() + ")")
     Фігури.Перемістити(хрест1, x, y)
     Фігури.Перемістити(хрест2, x, y)
та КрутитиОб'єкт () =
   для i = iMin до iMax-1 зробити
     нехай x = об'єкти.[i].X
     нехай y = об'єкти.[i].Y + 5
     нехай tx = Математика.Floor(Черепаха.X) |> int
     нехай ty = Математика.Floor(Черепаха.Y) |> int
     нехай д = Математика.КвадратнийКорінь(float (Математика.Ступінь(tx - x, 2) + Математика.Ступінь(ty - y, 2))) |> int
     якщо д < (розмір.[об'єкти.[i].Род] + 16) / 2 тоді
       виявленоЗіткнення <- істина   
     якщо y > гв тоді
       минуло <- минуло + 1
       Фігури.УстановитьТекст(рахунок, string минуло)
       Фігури.Видалити(об'єкти.[i].НазваФігури)
       iMin <- i + 1
     інакше
       Фігури.Перемістити(об'єкти.[i].НазваФігури, x, y)
       об'єкти.[i].X <- x
       об'єкти.[i].Y <- y   
та ДодатиОб'єкт () =   
   iMax <- iMax + 1
   ГрафичнеВікно.ШиринаПера <- 1.0
   нехай род = Математика.ОтриматиВипадковеЧисло(3)
   ГрафичнеВікно.КолірПензлика <- колір.[род]
   нехай рз = розмір.[род]
   нехай назваФігури = Фігури.ДодатиПрямокутник(рз, рз)
   нехай x = Математика.ОтриматиВипадковеЧисло(гш - 20) + 10
   нехай y = -20
   об'єкти.Add({ X = x; Y=y; Род=род; НазваФігури=назваФігури})
   Фігури.Перемістити(назваФігури, x, y)
   Фігури.Повернути(назваФігури, Математика.ОтриматиВипадковеЧисло(360))
